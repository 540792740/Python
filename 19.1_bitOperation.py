'''
&   与
|   或
^   异或
~   取反
<<  左移  *bit
>>  右移  /bit

树莓派
'''

num =  5
print(num<<1)
print(num>>1)

num = 0b00000101
print(num)

# 5     0000 0101
# 3     0000 0011
# 5&3   0000 0001   1
# 5|3   0000 0111   7
# 5^3   0000 0110   6
# 5~    1111 1010   1111 1001   0000 0110   所以是-6

a = 5
b = 3
print(a & b)
print(a | b)
print(a ^ b)
print(-a)